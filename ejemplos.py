# FUNCIONES DEL MÃ“DULO MATH
from math import ceil, floor, trunc

x = 1.4
y = 2.6

print('ceil (entero mayor):')
print(ceil(x), ceil(y))
print(ceil(-x), ceil(-y))
print('floor (entero menor):')
print(floor(x), floor(y))
print(floor(-x), floor(-y))
print('trunc (truncado a entero):')
print(trunc(x), trunc(y))
print(trunc(-x), trunc(-y))

